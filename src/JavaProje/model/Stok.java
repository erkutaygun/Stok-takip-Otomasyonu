package JavaProje.model;

public class Stok {
    private int stokID;
    private String stokAdet;
    private int urunID;

    public int getStokID() {
        return stokID;
    }

    public void setStokID(int stokID) {
        this.stokID = stokID;
    }

    public String getStokAdet() {
        return stokAdet;
    }

    public void setStokAdet(String stokAdet) {
        this.stokAdet = stokAdet;
    }

    public int getUrunID() {
        return urunID;
    }

    public void setUrunID(int urunID) {
        this.urunID = urunID;
    }
}
